int	ft_atoi(char *str)
{
	int	sign;
	int	ret;
	int	i;

	sign = 1;
	ret = 0;
	i = 0;
	while (str[i] < '0' || str[i] > '9')
	{
		if (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
				sign *= -1;
		}
		if (str[i] == '\0')
			return (0);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret = (str[i] - '0') + (ret * 10);
		i++;
	}
	return (ret * sign);
}
